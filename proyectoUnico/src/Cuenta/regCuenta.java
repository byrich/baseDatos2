    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        this.jCheckBox1.setSelected(false);
        String actual = dpi.getText();
        if (actual.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingresar un DPI");
        }
        else{
            int ret = api.buscarCliente(actual);
            if (ret == 1){
                elDpi = actual;
                this.jCheckBox1.setSelected(true);
                JOptionPane.showMessageDialog(null, "Usuario encontrado!");
            }
            else if (ret == -1){
                JOptionPane.showMessageDialog(null, "El usuario: " +actual+ " no existe");
            }
            else{
                JOptionPane.showMessageDialog(null, "Intente mas tarde");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


