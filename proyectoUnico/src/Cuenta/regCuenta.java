    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (!this.jCheckBox1.isSelected()){
            JOptionPane.showMessageDialog(null, "Ningun usuario encontrado");
        }
        else{
            int ret = api.agregarCuenta(elDpi,this.jComboBox1.getSelectedIndex());
            if (ret == 1){
                JOptionPane.showMessageDialog(null, "Registro Creado!");
            }
            else if (ret == -1){
                JOptionPane.showMessageDialog(null, "El usuario: " +elDpi+ " ya tiene una cuenta de este tipo");
            }
            else if (ret == -2){
                JOptionPane.showMessageDialog(null, "Intente mas tarde");
            }
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

