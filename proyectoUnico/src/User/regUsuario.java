    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        conexion global = conexion.getInstance();
        if (dpi.getText().isEmpty() || nombre.getText().isEmpty() || dia.getText().isEmpty() || mes.getText().isEmpty()|| anio.getText().isEmpty() || foto == null || firma == null ){
            JOptionPane.showMessageDialog(null, "Ningun campo puede estar vacio!");
        }
        else
        {
            String dpI = dpi.getText();
            String nombrE = nombre.getText();
            String date = dia.getText()+"/"+mes.getText()+"/"+anio.getText();
            if (global.evaluarCadena(nombrE)){
                int ret = api.agregarCliente(dpI, nombrE, date,foto,firma);
                if (ret == 1){
                    this.dpi.setText("");
                    this.nombre.setText("");
                    this.dia.setText("");
                    this.mes.setText("");
                    this.anio.setText("");
                    JOptionPane.showMessageDialog(null, "Registro Creado!");
                }
                else if (ret == -1){
                    JOptionPane.showMessageDialog(null, "El usuario: " +dpI+ " ya existe");
                }
                else if (ret == -2){
                    JOptionPane.showMessageDialog(null, "Intente mas tarde");
                }
                else{
                    JOptionPane.showMessageDialog(null, "Alguna imagen no existe!");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "El nombre contiene caracteres no permitidos");
            }
        }
    }//GEN-LAST:event_submitActionPerformed

